public class Musica extends Midia {
    // Atributos
    String artista;
    double notas;

    // Getters e Setters

    public String getArtista() {
        return this.artista;
    }

    public void setArtista(String artista) {
        this.artista = artista;
    }

    public double getNotas() {
        return this.notas;
    }

    public void setNotas(double notas) {
        this.notas = notas;
    }
}
